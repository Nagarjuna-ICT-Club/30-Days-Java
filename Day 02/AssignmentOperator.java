
public class AssignmentOperator {
    public static void main(String[] args) {
        int a = 5; // x is 5

        int b = 5;
        b += 3; // x is now 8

        int c = 5;
        c -= 3; // x is now 2

        int d = 5;
        d *= 3; // x is now 15

        int e = 15;
        e /= 3; // x is now 5

        int f = 15;
        f %= 4; // x is now 3
        
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);
    }
}
