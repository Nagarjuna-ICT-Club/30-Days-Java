public class PrimitiveDataTypes {
    public static void main(String[] args) {
        byte aByte = 100;            // byte example
        short aShort = 10000;        // short example
        int anInt = 100000;          // int example
        long aLong = 100000L;        // long example
        float aFloat = 10.5f;        // float example
        double aDouble = 10.5;       // double example
        char aChar = 'A';            // char example
        boolean aBoolean = true;     // boolean example
        
        System.out.println("byte: " + aByte);
        System.out.println("short: " + aShort);
        System.out.println("int: " + anInt);
        System.out.println("long: " + aLong);
        System.out.println("float: " + aFloat);
        System.out.println("double: " + aDouble);
        System.out.println("char: " + aChar);
        System.out.println("boolean: " + aBoolean);
    }
}
